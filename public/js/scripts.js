document.getElementById('contenido').onmouseover = function(){
    document.getElementById('rubrica-contenido').style.display = 'block';
    document.getElementById('rubrica-general').style.display = 'none';
}
document.getElementById('contenido').onmouseout = function(){
    document.getElementById('rubrica-contenido').style.display = 'none';
    document.getElementById('rubrica-general').style.display = 'block';
}

